package com.xfwl.beans.menus;

/**
 * 基础按钮
 * 
 * @author Jason
 * 
 */
public class BasicButton {
	private String name;
	private String type;

	public String getName() {
		return this.name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return this.type;
	}

	public void setType(String type) {
		this.type = type;
	}
}
